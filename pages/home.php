<h1>Verteilerposition für 100-Meter-Hindernisbahn</h1>

<h3>Sinn dieser Aktion</h3>
<p>Diese Seite soll dazu dienen, dass ihr alle eure Verteilerpositionen eintragen könnt und somit vor dem Wettkampf eine Liste aller Positionen ausgedruckt werden kann. Ich weiß, dass ihr eure Vertrauensperson für das Stellen des Verteilers habt, aber es kann immer mal was dazwischen kommen. Deshalb wollen wir eine vollständige Liste für alle Fälle haben.</p>

<h3>Vorgehensweise</h3>
<ol>
	<li>Auf Seite <a href="?page=new">Neuer Eintrag</a> gehen</li>
	<li>Mittels Rotationsfunktion (⟳⟲) den Verteiler drehen, so wie ihr ihn haben wollt. Laufrichtung ist von unten nach oben.</li>
	<li>Mittels Positionierungsfunktion (⇐⇒) den Verteiler verschieben, wie er auf der Bahn stehen soll.</li>
	<li>Die Mannschaft auswählen (Männer, Frauen)</li>
	<li>Den eigenen Namen eintragen (Bitte mit Groß- und Kleinschreibung)</li>
	<li>Auf "Speichern" klicken</li>
	<li>Ihr werde zur Liste weitergeleitet. Wenn ihr euch versichern wollt, dass alles korrekt abgespeichert wurde und wie es später auf dem ausgedruckten Zettel aussieht, könnt ihr oben auf <strong>PDF</strong> klicken.</li>
	<li>Sollte euch die Position nicht mehr gefallen, könnt ihr sie ändern, wie unten beschrieben:</li>
</ol>

<h3>Änderung der eingetragenen Position</h3>
<p>Folgt einfach den Anweisungen von oben und tragt den gleichen Namen ein. Beim Speichern werdet ihr gefragt, ob ihr die aktuelle Position überschreiben wollt.</p>
<p>Solltet ihr eine falsche Position oder einen falschen Namen löschen wollen, schreibt mir kurz eine E-Mail an: georf@georf.de</p>

<h3>Fehler und Verbesserungen</h3>
<p>Solltet ihr Fehler entdecken oder Verbesserungsvorschläge haben, dann schreibt mir an die oben genannte Adresse oder nutzt das <a href="http://www.feuerwehrsport-teammv.de/kontakt/">Kontaktformular</a>.</p>


